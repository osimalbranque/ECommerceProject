<h3>Explication</h3>
<p>
    On a choisi les frameworks Bootstrap et CodeIgniter.
    CodeIgniter parce qu'il est très personalisable, permet une architecture MVC et plus rapide en écriture avec son auto-complétion par exemple.
    Bootstrap parce qu'il donne la possibilité de rendre adaptatif un site web sur n'importe quel support.
</p>
